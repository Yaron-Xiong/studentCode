	public int shipWithinDays(int[] weights, int days) {
		int left = 0;
		int right = 0;
		for (int weight : weights) {
			left = Math.max(left, weight);
			right += weight;
		}
		while (left <= right) {
			int mid = (left + right) >> 1;
			int f = f(weights, mid);
			if (f > days) {
				left = mid + 1;
			} else {
				right = mid - 1;
			}
		}
		return left;
	}

	private int f(int[] weights, int x) {
		int days = 1;
		int cap = 0;
		for (int weight : weights) {
			if (cap + weight > x) {
				days++;
				cap = 0;
			}
			cap += weight;
		}
		return days;
	}
